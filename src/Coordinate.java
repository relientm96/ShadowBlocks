package project2;

/**
 * A class that contains a coordinate to make coordinate transfer easier 
 * @author Matthew
 *
 */

public class Coordinate {
	
	private float x;
	private float y;
	
	public Coordinate(){

	}
	
	public void setX(float var){
		x=var;
	}
	
	public void setY(float var){
		y= var;
	}
	
	public float getX(){
		return x;
	}
	
	public float getY(){
		return y;
	}

}
